    def desenha(self, tela):
        for numero_linha, linha in enumerate(self.matriz):
            self.pintar_linha(tela, numero_linha, linha)

    def adicionar_movivel(self, obj):
        self.moviveis.append(obj)


    def pintar_linha(self, tela, numero_linha, linha):
        for numero_coluna, coluna in enumerate(linha):
            x = numero_coluna * TAMANHO_NO
            y = numero_linha * TAMANHO_NO + 4 * TAMANHO_NO
            half = TAMANHO_NO // 2
            cor = PRETO
            if coluna == 2:
                cor = AZUL
            pygame.draw.rect(tela, cor, (x, y, TAMANHO_NO, TAMANHO_NO), 0)
            if coluna == 1:
                pygame.draw.circle(tela, AMARELO, (x + half, y + half), TAMANHO_NO // 10, 0)

    def calcular_regra(self):
        for movivel in self.moviveis:
            lin = int(movivel.linha)
            col = int(movivel.coluna)
            lin_intencao = int(movivel.linha_intencao)
            col_intencao = int(movivel.coluna_intencao)

            if 0 <= col_intencao < NUM_COLUNAS and 0 <= lin_intencao < NUM_LINHAS and self.matriz[lin_intencao][col_intencao] != 2:
                movivel.aceitar_movimento()
            else:
                movivel.recusar_movimento()

    def processar_eventos(self, evts):
        for e in evts:
            if e.type == pygame.QUIT:
                exit()
